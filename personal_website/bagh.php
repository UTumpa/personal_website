<p>
    The Bengal tiger's coat is yellow to light orange, with stripes ranging from dark brown to black; the belly and the
    interior parts of the limbs are white, and the tail is orange with black rings. The white tiger is a recessive mutant
    of the Bengal tiger, which is reported in the wild from time to time in Assam, Bengal, Bihar and especially from the
    former State of Rewa. However, it is not to be mistaken as an occurrence of albinism. In fact, there is only one fully
    authenticated case of a true albino tiger, and none of black tigers, with the possible exception of one dead specimen
    examined in Chittagong in 1846.[10]

    Male Bengal tigers have an average total length of 270 to 310 cm (110 to 120 in) including the tail, while females measure
    240 to 265 cm (94 to 104 in) on average.[11] The tail is typically 85 to 110 cm (33 to 43 in) long, and on average,
    tigers are 90 to 110 cm (35 to 43 in) in height at the shoulders.[12] The weight of males ranges from 180 to 258 kg (397
    to 569 lb), while that of the females ranges from 100 to 160 kg (220 to 350 lb).[11] The smallest recorded weights for
    Bengal tigers are from the Bangladesh Sundarbans, where adult females are 75 to 80 kg (165 to 176 lb).[13] Bengal tigers
    have exceptionally stout teeth, and the canines are the longest among all living felids; measuring from 7.5 to 10 cm
    (3.0 to 3.9 in) in length.[14]
    Records

    Two tigers shot in Kumaon and near Oude at the end of the 19th century allegedly measured more than 12 ft (370 cm).
    But at the time, sportsmen had not yet adopted a standard system of measurement; some would measure between pegs while
    others would round the curves.[15]

    In the beginning of the 20th century, a male Bengal tiger was shot in central India with a head and body length of 221
    cm (87 in) between pegs, a chest girth of 150 cm (59 in), a shoulder height of 109 cm (43 in) and a tail length of 81
    cm (32 in), which was perhaps bitten off by a rival male. This specimen could not be weighed, but it was calculated
    to weigh no less than 272 kg (600 lb).[16]

    A heavy male weighing 570 lb (260 kg) was shot in northern India in the 1930s.[17] However, the heaviest known tiger was
    a huge male killed in 1967 that weighed 388.7 kg (857 lb) and measured 322 cm (127 in) in total length between pegs,
    and 338 cm (133 in) over curves. This specimen is on exhibition in the Mammals Hall of the Smithsonian Institution.
    [18] In 1980 and 1984, scientists captured and tagged two male tigers in Chitwan National Park that weighed more than
    270 kg (600 lb).[19]
    Genetic ancestry
    Bengal tigers are defined by three distinct mitochondrial nucleotide sites and 12 unique microsatellite alleles.
    The pattern of genetic variation in the Bengal tiger corresponds to the premise that they arrived in India approximately
    12,000 years ago.[20] This is consistent with the lack of tiger fossils from the Indian subcontinent prior to the late
    Pleistocene and the absence of tigers from Sri Lanka, which was separated from the subcontinent by rising sea levels in
    the early Holocene.[21]
