In his Banglar Kavigan, Sajani Kanta Das said, “Kavigan was born out of a synthesis of
various art forms prevalent in different parts of Bengal at different times having peculiar
names such as tarja, panchali, kheur, akhrai, half akhrai, full akhrai, danra kavigan, basa kavigan,
dhap kirtan, tappa, Krishna jatra, tukkagiti etc.”[2] Various literary researchers such as Ishwar Chandra
Gupta and Dr. Harekrishna Mukhopadhyay have dwelt at length on the origins and development of kavigan.[2]

Dr. Sushil Kumar Dey opines, “The existence of kabi songs may be traced to the beginning of the 18th century
or even beyond it to the 17th; but the flourishing period of the Kabiwalas was between 1760 and 1830.”[2] As
the religious and ritualistic content in Bengali poetry wore out there was a tendency to break away from the
traditional Vaishnava poetry but the real breakthrough came only with the introduction of the printing press
in mid–18th century. From the close of the 18th century for more than half a century the new kavi–poetry and
panchali reigned supreme in the Kolkata region and almost threatened to sweep everything else in literature.[4]
However, while Kavigan lost its supremacy in Kolkata, it retained its position in rural Bengal.[2]The mug of Mymensingh
is better, and the kai fish of Khulna;Dhaka's pataksir is better, and the yoghurt of Bankura;The sweetmeat makers
of Krishnanagar are better, and the mangoes of Maldaha;Ulo's male monkeys are better, and the blackberries of
Murshidabad;The fathers-in-law of Rangpur are better, and the sons–in–law of Rajshahi;The boats of Noakhali are
better, and the midwives of Chittagong;The Kayets of Dinajpur are better, and the wine sellers of Howrah;The
Vaishnavas of Pabna are better, and the mudi of Faridpur;The cultivators of Burdwan are better, and the milkmen
of 24-Paraganas;The girls of Guptipara are better–beware your line will soon become extinct.The fighters and ruffians
of Hughli are better, and the buttermilk of Birbhum.It is better if the rhythm of the dhak stops and all utter the name
of Hari in chorus.Banglapedia[5]
<p>
Dr. Sushil Kumar Dey has a word of praise for the kaviyals, “These poets were, no doubt, born among the people
