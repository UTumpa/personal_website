                    Kavigan is normally sung by two groups. Each group is led by a kaviyal or sarkar.
                    The accompanying singers called dohars often repeat what the leader said.[1] A kavigan
                    programme starts with bandana (evocation) or gurudever geet (song of the sect patron).
                    The bandana can be directed to or be in praise of Saraswati, Ganesh, people, and the
                    audience, as deemed fit by a particular kaviyal. This is followed by Radha–Krishna related
                    song, some call it agamani. Then songs on four subjects are sung: Sakhi sambad, biraha, lahar
                    and kheur.Sakhi Sambad deals with the love-songs related to Radha-Krishna.
