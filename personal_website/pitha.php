            Aarisa piṭha (Oriya: ଆରିସା ପିଠା): deep-fried pitha made from rice flour with jaggery or sugar.
            Manda Pitha (Oriya: ମଣ୍ଡାପିଠା): steamed pitha made from rice flour, often with a grated coconut and jaggery/sugar filling or a fresh cheese (chhena) filling.
            Kakara pitha (Oriya: କାକରା ପିଠା): very popular fried pancake made from wheat flour/semolina, often with a sweet cocunut filling.[1][2]
            Enduri Pitha (Oriya: ଏଣ୍ଡୁରି ପିଠା): pitha made from rice flour and black gram, often with a jaggery and coconut or cheese filling, steamed in a turmeric leaf. Enduri pitha is a special delicacy of the Prathamastami festival.
            Chakuli Pitha (Oriya: ଚକୁଳି ପିଠା): (chakuli) thin/thick pancake(set dosa) made from fermented rice and black gram, often eaten for breakfast. Thinner varieties are called saru chakuli, and resemble plain dosas. Often served with dalama (Oriya: ଡାଲମା) (vegetable mixed with pulses)nadia chatni (coconut chutney) or kheeri (Oriya: ଖିରି), a rice dish.
            Poḍa piṭha (Oriya: ପୋଡ଼ ପିଠା) (lit. "burnt pitha"): slow-cooked pitha made from fermented rice and black gram, with chopped coconut. Its crust is slightly burnt, while the inside is soft and white,mostly during Raja Parba.
            Chitau piṭha (Oriya: ଚିତଉ ପିଠା): fried pancake made from rice flour, coconut, and milk, often offered in pujas to Lord Jagannath on Chitalagi Amabasya (or Chitau Amabasya).
            Gaintha godi (Oriya: ଗଇଁଠା ଗୋଡ଼ି): Small balls made of rice flour dipped in seasioned sweet milk.speciality of pausha amavasya(baula amavasya)[12]
            Karanji (Oriya: କରଞ୍ଜି): A dumpling made of maida and stuffed with coconut,cashew raisins.
            Malpua (Oriya: ମାଲପୁଆ): Sweetened deep fried batter of a mixture of bananas and flour
            Suji Kakara Pitha (Oriya: ସୁଜି କାକରା ପିଠା): A sweet dish made of suji with coconut stuffing
            Tala Pitha (Oriya: ତାଳ ପିଠା): A sweet dish made of palm fruit and rice batter

