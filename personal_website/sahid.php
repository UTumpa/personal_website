    The First Shaheed Minar The first Shaheed Minar, built on February 22, 1952. It was demolished by Pakistan Police and
    Army four days later.The first Shaheed Minar was built immediately after the events of February 21, 1952. According to
    Dr. Sayeed Haider,a main planner and the designer of the first Shaheed Minar, the decision to build it was first made e
    students ofDhaka Medical College. Shaheed Minar is situated near Dhaka Medical College Hospital and in the Dhaka University area.
    It is adjacent to the Mathematics department of Dhaka University. It is only 0.5 kilometres (0.3 mi) away from Shahbag
    and 0.25 km (0.16 mi) distant from Chankharpul. Shaheed Minar is an outstanding monument of Bangladesh. It was built to
    tribute the martyrs who given up their life for language. The main incident had been occurring inside of Dhaka medical
    college hospital. So a decision was taken to build a memorial adjacent to DMCH. The planning started at midnight on February 22, and the work started the next day. This Minar was sponsored by Pearu Sardar, one of the old Dhaka panchayet sardars, when some of the students asked his help at midnight of 22 February, to contribute the raw materials needed to build the monument. Although curfew was in place, students started building the Minar in the afternoon of February 23. They worked through the night and finished it at dawn. A hand written paper was attached to the Minar with "Shaheed Smritistombho" written on it. The original Minar measured 10 by 6 feet (3.0 m × 1.8 m).[3] The Minar was inaugurated by the father of Sofiur Rahman, killed during the massacre. It was demolished within a few days by the police and Pakistani Army.[4] Smaller versions of the memorial were built in other places of the country.[5] Two years after the first monument was destroyed by the police, a new Shaheed Minar (Monument of Martyrs) was constructed in 1954 to commemorate the protesters who lost their lives. This minaar (monument) was inaugurated by Natyaguru Nurul Momen. Work on a larger monument designed by the architect Hamidur Rahman began in 1957 with the support of the United Front ministry.
    Shaheed Minar (1963-1971)Following the formation of local government by the United Front - led by A.K. Fazlul Huq and t
    he Awami League,the anniversary of 21 February in 1956 was observed openly and widely [6] and it became possible to
    constructthe new monument. Sculptor Hamidur Rahman created the design of Shaheed Minar under which construction was started
    in 1957. Hamidur Rahman’s model was a huge complex on a large area of land in the yard of Dhaka Medical College Hostel.
    The enormous design included a half-circular column to symbolize the mother with her fallen sons standing on the monument's
    central dais. Yellow and deep blue pieces of stained glass, symbolizing eyes reflecting the sun, were also to be placed in
    the columns. The marble floor was designed to reflect the moving shadows of the columns. The basement of the Minar also
    included a 1,500-square-foot (140 m2) fresco depicting the history of the language movement. A railing decorated with
    Bengali alphabet was to be constructed in front. Two footmarks coloured red and black, indicating the two opposite forces, were also in the design. Besides this a museum and a library were also included in Hamidur Rahman's design. A fountain shaped like an eye was also to be constructed.[5] Rahman specifically designed the materials of the monument to withstand the area's tropical climate.
    Construction started in November 1957, under the supervision of Hamidur Rahman and Novera Ahmed. Most of the work,
    including the basement, platform, some of the columns with the rails, footprints and some of the murals were also
    finished when martial law was declared in the area, and the construction was forced to a halt. Construction work was
    completed in 1963, leaving much of Rahman's design unfinished. It was inaugurated on February 21, 1963, by the mother of
    Abul Barkat, Hasina Begum. The Minar was severely damaged by the Pakistan Army during the Bangladesh Liberation War in
    1971. The columns were destroyed during the fighting.[7] The Pakistani Army crushed the Minar and placed over the rubble
    a signboard reading "Mosque".Re-establishment of Shaheed Minar and current statenIn 1972, a committee headed by the then
    president Abu Sayeed Chowdhury was formed and renovation work began.The original sketch was ignored, while the Construction
    and Building Directorate followed the 1963 design.[8]The construction went quickly, according to a modified design from 1963.[5] The murals
    destroyed by the Pakistaniarmy were not restored and the basement was sealed off. Hamidur Rahman’s original design was
    not approved by the directorate in the renovation work. In the mid-eighties, the monument underwent further renovation
    under the supervision of the then Department of Architecture chief architect SHM Abul Bashar, which extended the area
    of the Shaheed Minar premises, giving it a square shape from a triangular one. Quarters concerned demanded proper
    implementation of the design by Hamidur Rahman with the help of sculptor Novera Ahmed and Danish architect Gean Deleuran.
    ASM Ismail said that because of the extension, two entrances to the basement murals were permanently closed, and after
    remaining in an abandoned state for 15 years, the murals had lost much of their gloss. In the 1983 renovation, the
    original poor materials were lined with marble stone. A museum and library were also featured in the original plan.
    On August 25, 2010, the High Court issued nine directives for the maintenance and renovation of the Shaheed Minar and
    asked the Public Works Department to establish a museum and a library on its premises.[8] Despite some flaws of proportional
    ity, the Shaheed Minar still stands high. It is one of the city's most important monuments. The Shaheed Minar of
    Dhaka has a very close association with the city's cultural history. It also happens to be one of the primary Dhaka
    tourist attractions and is visited by thousands of tourists throughout the year. It is one of the most well-maintained
    monuments in Bangladesh. Special care is taken each year on the occasion of 21 February (Ekushey February). The premises
    are washed and cleaned thoroughly. Artists of the Faculty of Fine Arts of Dhaka University then colorfully paint the Central
    Shaheed Minar premises with intricate designs.Thus the Shaheed Minar
    premises are colorful throughout the year.
